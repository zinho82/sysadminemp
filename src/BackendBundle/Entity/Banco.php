<?php

namespace BackendBundle\Entity;

/**
 * Banco
 */
class Banco
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $numeroCuentya;

    /**
     * @var \BackendBundle\Entity\Config
     */
    private $nombreBanco;

    /**
     * @var \BackendBundle\Entity\Config
     */
    private $tipoCuenta;

    /**
     * @var \BackendBundle\Entity\Empresa
     */
    private $empresa;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set numeroCuentya
     *
     * @param string $numeroCuentya
     *
     * @return Banco
     */
    public function setNumeroCuentya($numeroCuentya)
    {
        $this->numeroCuentya = $numeroCuentya;

        return $this;
    }

    /**
     * Get numeroCuentya
     *
     * @return string
     */
    public function getNumeroCuentya()
    {
        return $this->numeroCuentya;
    }

    /**
     * Set nombreBanco
     *
     * @param \BackendBundle\Entity\Config $nombreBanco
     *
     * @return Banco
     */
    public function setNombreBanco(\BackendBundle\Entity\Config $nombreBanco = null)
    {
        $this->nombreBanco = $nombreBanco;

        return $this;
    }

    /**
     * Get nombreBanco
     *
     * @return \BackendBundle\Entity\Config
     */
    public function getNombreBanco()
    {
        return $this->nombreBanco;
    }

    /**
     * Set tipoCuenta
     *
     * @param \BackendBundle\Entity\Config $tipoCuenta
     *
     * @return Banco
     */
    public function setTipoCuenta(\BackendBundle\Entity\Config $tipoCuenta = null)
    {
        $this->tipoCuenta = $tipoCuenta;

        return $this;
    }

    /**
     * Get tipoCuenta
     *
     * @return \BackendBundle\Entity\Config
     */
    public function getTipoCuenta()
    {
        return $this->tipoCuenta;
    }

    /**
     * Set empresa
     *
     * @param \BackendBundle\Entity\Empresa $empresa
     *
     * @return Banco
     */
    public function setEmpresa(\BackendBundle\Entity\Empresa $empresa = null)
    {
        $this->empresa = $empresa;

        return $this;
    }

    /**
     * Get empresa
     *
     * @return \BackendBundle\Entity\Empresa
     */
    public function getEmpresa()
    {
        return $this->empresa;
    }
    /**
     * @var string
     */
    private $numeroCuenta;


    /**
     * Set numeroCuenta
     *
     * @param string $numeroCuenta
     *
     * @return Banco
     */
    public function setNumeroCuenta($numeroCuenta)
    {
        $this->numeroCuenta = $numeroCuenta;

        return $this;
    }

    /**
     * Get numeroCuenta
     *
     * @return string
     */
    public function getNumeroCuenta()
    {
        return $this->numeroCuenta;
    }
}
